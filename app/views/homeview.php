<div>
	<h2>Welcome</h2>
	<p>
		This is the default home page of the Jiaoyu web framework.  Jiaoyu is a very simple MVC
		web application framework written in PHP.  Compared to many other readily available
		frameworks, it is not so fully featured.  There is a very good reason for this.  <strong>
		Jiaoyu is not meant to be used in production code!</strong>  Instead it was meant to be
		used for education.  I wrote it specifically to learn how to write an MVC framework and
		attempted to write it in a way that made the code easy to follow.  Apart from some of the
		routing code and some of the ORM system, I think I have succeeded in this goal.  So use
		this framework by reading the code and seeing one of many possible ways to implement
		the MVC pattern.  If you use this in a production system it is entirely at your own risk.
		<strong>There will be no help forthcoming</strong> - consider this fair warning.
	</p>

	<p>
		Instructions on framework setup (which you've probably already done properly if you're seeing this)
		and use can be found at <a href="https://github.com/gabriel-comeau/jiaoyu">https://github.com/gabriel-comeau/jiaoyu</a>.
	</p>
</div>
